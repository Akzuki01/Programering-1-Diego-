package PrimitivaDataTyper;

public class Primitiva7 {

	public static void main(String[] args) {
		
		int a, b, temp;
		a = 15;
		b = 27;
		
		System.out.println("before swapping : a, b = " +a+" , "+ +b);
		
		temp = a;
		a = b;
		b = temp;
		System.out.println( "After swapping : a, b = " +a+" , " + +b );

	}

}
